'''
이스케이프 문자 => 이렇게 출력값을 나오게 할 수 있어.
     \n -> 줄바꿈
     \t -> 탭
     \b -> 백스페이스
     \\ -> \
     \' -> 작은 따옴표
     \" -> 큰 따옴표
     \r -> 줄 바꿈, 커서를 앞으로 이동
     \f -> 줄 바꿈, 커서를 다음 줄로 이동
     \a -> 벨소리
     \v -> 수직 탭
'''

print('Hello \'World\'')
print('Hello \"World\"')
print('*\n**\n***')
